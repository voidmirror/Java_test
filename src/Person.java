public class Person {
    private int weight;
    private int height;

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getWeight() {
        return weight;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getHeight() {
        return height;
    }

    public Person() {
        this.weight = 0;
        this.height = 0;
    }
}
