public class Puple {
    private int weight;
    private int height;
    private int behaviorRating;

    // constructors------------------------------------------

    public Puple() {
        this.weight = 0;
        this.height = 0;
    }

    // methods-----------------------------------------------

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getWeight() {
        return weight;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getHeight() {
        return height;
    }

    /**
     * Default constructor
     */


}
